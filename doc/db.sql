/**
 * 菜单
 */
CREATE TABLE `sys_menu` (
  `menu_id` varchar(100) NOT NULL AUTO_INCREMENT,
  `parent_id` varchar(100) COMMENT '父菜单ID，一级菜单为0',
  `name` varchar(50) COMMENT '菜单名称',
  `url` varchar(200) COMMENT '菜单URL',
  `perms` varchar(500) COMMENT '授权(多个用逗号分隔，如：user:list,user:create)',
  `type` varchar(2) COMMENT '类型   0：目录   1：菜单   2：按钮',
  `icon` varchar(50) COMMENT '菜单图标',
  `order_num` int COMMENT '排序',
  PRIMARY KEY (`menu_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='菜单管理';

/**
 * 系统用户表
 */
CREATE TABLE `sys_user` (
  `user_id` varchar(100) NOT NULL AUTO_INCREMENT,
  `username` varchar(50) NOT NULL COMMENT '用户名',
  `password` varchar(100) COMMENT '密码',
  `email` varchar(100) COMMENT '邮箱',
  `mobile` varchar(100) COMMENT '手机号',
  `status` varchar(10) COMMENT '状态  0：禁用   1：正常',
  `create_time` datetime COMMENT '创建时间',
  PRIMARY KEY (`user_id`),
  UNIQUE INDEX (`username`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='系统用户';

/**
 * 角色表
 */
CREATE TABLE `sys_role` (
  `role_id` varchar(100) NOT NULL AUTO_INCREMENT,
  `role_name` varchar(100) COMMENT '角色名称',
  `remark` varchar(100) COMMENT '备注',
  `create_time` datetime COMMENT '创建时间',
  PRIMARY KEY (`role_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='角色';

/**
 * 用户和角色对应关系
 */
CREATE TABLE `sys_user_role` (
  `id` varchar(100) NOT NULL AUTO_INCREMENT,
  `user_id` varchar(100) COMMENT '用户ID',
  `role_id` varchar(100) COMMENT '角色ID',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='用户与角色对应关系';

/**
 * 角色和菜单关系
 */
CREATE TABLE `sys_role_menu` (
  `id` varchar(100) NOT NULL AUTO_INCREMENT,
  `role_id` varchar(100) COMMENT '角色ID',
  `menu_id` varchar(100) COMMENT '菜单ID',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='角色与菜单对应关系';


/**
 * 系统配置信息
 */
CREATE TABLE `sys_config` (
	`id` varchar(100) NOT NULL AUTO_INCREMENT,
	`key` varchar(50) COMMENT 'key',
	`value` varchar(2000) COMMENT 'value',
	`status` tinyint DEFAULT 1 COMMENT '状态   0：隐藏   1：显示',
	`remark` varchar(500) COMMENT '备注',
	PRIMARY KEY (`id`),
	UNIQUE INDEX (`key`)
) ENGINE=`InnoDB` DEFAULT CHARACTER SET utf8 COMMENT='系统配置信息表';


